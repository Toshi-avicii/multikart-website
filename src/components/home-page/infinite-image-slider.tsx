export default function InfiniteImageSlider({  }) {
    const containerRef = useRef<HTMLDivElement>(null);
    const [x, setX] = useState(0);
    const [paused, setPaused] = useState(false);
    const speed = 1;

    useAnimationFrame(() => {
        if (!containerRef.current || paused) return;

        const scrollWidth = containerRef.current.scrollWidth / 2;
        setX((prev) => {
            const next = prev - speed;
            return next <= -scrollWidth ? 0 : next;
        });
    });

    return (
        <>
            <div className="slider-container">
                <motion.div
                    className="slider-track"
                    ref={containerRef}
                    style={{ x }}
                    onMouseEnter={() => setPaused(true)}
                    onMouseLeave={() => setPaused(false)}
                >
                    {[...row1Imgs, ...row1Imgs].map((src, i) => (
                        <div key={i} className="slider-image-wrapper">
                            <img
                                src={src}
                                alt={`img-${i}`}
                                className="slider-image cursor-pointer"
                            />
                        </div>
                    ))}
                </motion.div>
            </div>
        </>
    );
}
